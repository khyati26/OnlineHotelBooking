package com.narola.hotelbooking.Hotel;

public class CancellationRules {
	private int id;
	private int hotelid;
	private int	hours;
	private int refundpercentsge;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getHotelid() {
		return hotelid;
	}
	public void setHotelid(int hotelid) {
		this.hotelid = hotelid;
	}
	public int getHours() {
		return hours;
	}
	public void setHours(int hours) {
		this.hours = hours;
	}
	public int getRefundpercentsge() {
		return refundpercentsge;
	}
	public void setRefundpercentsge(int refundpercentsge) {
		this.refundpercentsge = refundpercentsge;
	}
	
	
	

}
